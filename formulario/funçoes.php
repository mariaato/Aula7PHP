<?php
function somar($numero1, $numero2) {
        $soma = $numero1 + $numero2;
        return $soma;
        }

        function subtracao($numero1, $numero2) {
            $subtracao = $numero1 - $numero2;
            return $subtracao;
        }

        function multiplicacao($numero1, $numero2) {
            $multiplicacao = $numero1 * $numero2;
            return $multiplicacao;
        }